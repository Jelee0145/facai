def build_model_user_prompt(
    product_type: str,
    country_name: str,
    platform: str,
    model_name: str,
    model_tagline: str,
    count: int,
    user_description: str = "",
) -> str:
    """构建模特图请求的用户消息"""
    desc_line = f"\n用户补充描述：{user_description}" if user_description else ""
    return f"""请参考上方的商品图片，生成 {count} 条模特展示图的生图提示词。

目标市场：{country_name}（{platform}）
拍摄风格：{model_name} - {model_tagline}
商品类型：{product_type}{desc_line}

每条提示词的场景、姿势、镜头必须不同。

输出 JSON 格式：
{{"prompts": ["填好的提示词1", "填好的提示词2", ...]}}"""


